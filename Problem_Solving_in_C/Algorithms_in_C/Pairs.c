void sort(int n,int arr[])
{
    for(int i=1;i<n;i++)
    {
        int current=arr[i];
        int j=i-1;
        while(arr[j]>current && j>=0)
        {
            arr[j+1]=arr[j];
            j--;
        }
        arr[j+1]=current;
    }
}
bool binary_search(int n, int arr[],int key)
{
    int s=0;
    int e=n;
    while(s<=e)
    {
        int mid=(s+e)/2;
        if(arr[mid]==key)
        {
            return true;
        }
        else if(arr[mid]>key)
        {
            e=mid-1;
        }
        else{
            s=mid+1;
        }
    }
    return false;
}
int pairs(int k, int arr_count, int* arr) 
{
    sort(arr_count, arr);
    int result=0;
    for(int i=0;i<arr_count;i++)
    {
        int x=arr[i]-k;
        if(binary_search(arr_count,arr,x))
        {
            result++;
        }
    }
    return result;
}

